primerNumero = int(input("Introduce un número entero: "))
segundoNumero = int(input("Introduce un segundo numero entero: "))
if primerNumero < 0 or segundoNumero < 0:
	print ("No se calcula la suma porque alguno de los números o los dos no son positivos")

else:
        print ("La suma de los número es:", primerNumero + segundoNumero)
